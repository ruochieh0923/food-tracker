def seed():
    init_db()
    inserted = skipped = 0
    with get_db() as db:
        for row in ROWS:
            category, restaurant, meal_name, rating_text, satiety, price_raw, notes, cal_raw, repurchase = row
            if not meal_name.strip():
                continue

            # Check duplicate
            exists = db.execute(
                "SELECT id FROM meals WHERE meal_name = ? AND (restaurant = ? OR (restaurant IS NULL AND ? = ''))",
                (meal_name.strip(), restaurant.strip() or None, restaurant.strip())
            ).fetchone()
            if exists:
                skipped += 1
                continue

            cal, cal_min, cal_max = parse_cal(cal_raw)
            price = parse_price(price_raw)
            rating_num = RATING_MAP.get(rating_text)
            rep = int(repurchase) if repurchase else None
            rest = restaurant.strip() or None
            sat = satiety.strip() or None
            rt = rating_text.strip() or None
            notes_clean = notes.strip() or None
            cat = category.strip() or None
            mn = meal_name.strip()

            db.execute(
                """INSERT INTO meals
                   (category, restaurant, meal_name, rating_text, rating, repurchase,
                    satiety, price, notes, calories, calories_min, calories_max, source)
                   VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?)""",
                (cat, rest, mn, rt, rating_num, rep, sat, price, notes_clean,
                 cal, cal_min, cal_max, "csv_import")
            )
            inserted += 1

    print(f"完成！匯入 {inserted} 筆，跳過 {skipped} 筆（重複）")


if __name__ == "__main__":
    seed()
