def parse_cal(val):
    if not val:
        return None, None, None
    import re
    val = val.strip().replace("kl", "").replace("kcal", "").replace("卡", "").strip()
    m = re.match(r'^(\d+(?:\.\d+)?)\s*[~\-–]\s*(\d+(?:\.\d+)?)$', val)
    if m:
        lo, hi = float(m.group(1)), float(m.group(2))
        return (lo + hi) / 2, lo, hi
    try:
        return float(val), None, None
    except ValueError:
        return None, None, None

def parse_price(val):
    if not val:
        return None
    import re
    val = re.sub(r'[NT$,\s]', '', val)
    try:
        return float(val)
    except ValueError:
        return None

